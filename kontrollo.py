dita_anglisht=input("Fut nje dite na anglisht").lower()

if dita_anglisht == "monday":
    print("E hene")
elif dita_anglisht == "tuesday":
    print("E Marte")
elif dita_anglisht == "wednesday":
    print("E Merkure")
elif dita_anglisht == "thursday":
    print("E Enjte")
elif dita_anglisht == "friday":
    print("E Premte")
elif dita_anglisht == "saturday":
    print("E Shtune")
elif dita_anglisht == "sunday":
    print("E Diel")
else:
    print("Fjala nuk eshte valide")
